package com.example.mypantryapp;

public class ExampleItem {
    private String mName;
    private String mBrand;

    public ExampleItem(String name, String brand) {
        mName = name;
        mBrand = brand;
    }

    public String getName() {
        return mName;
    }

    public String getBrand() {
        return mBrand;
    }

}
